public enum Operator
{
    /// <summary>
    /// Represents an Equals operator.
    /// </summary>
    Equals,

    /// <summary>
    /// Represents a GreaterThan operator.
    /// </summary>
    GreaterThan,

    /// <summary>
    /// Represents a LessThan operator.
    /// </summary>
    LessThan,

    /// <summary>
    /// Represents a GreaterThanOrEqual operator.
    /// </summary>
    GreaterThanOrEqual,

    /// <summary>
    /// Represents a LessThanOrEqual operator.
    /// </summary>
    LessThanOrEqual,

    /// <summary>
    /// Represents a Contains operator.
    /// </summary>
    Contains,

    /// <summary>
    /// Represents a StartsWith operator.
    /// </summary>
    StartsWith,

    /// <summary>
    /// Represents a EndsWith operator.
    /// </summary>
    EndsWith
}